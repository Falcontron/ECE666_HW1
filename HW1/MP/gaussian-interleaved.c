int NUM_PROCS;

void eliminate (double **A, int myid) {   
    double temp_row[N] = {0};

    for (int k = 0; k < N; ++k){              
        int cur_id = k % NUM_PROCS;

        if (myid == cur_id){
            for (int j = k+1; j < N; ++j){    
                A[k][j] /= A[k][k];
                temp_row[j] = A[k][j];
            }
            A[k][k] = 1.0;
            temp_row[k] = 1.0;
        }

        MPI_Bcast(temp_row, N, MPI_DOUBLE, cur_id, MPI_COMM_WORLD);

        for (int i = myid; i < N; i += NUM_PROCS){
            if (i > k){
                for (int j = k + 1; j < N; ++j){
                    A[i][j] -= A[i][k] * temp_row[j];
                }
                A[i][k] = 0.0;
            }
        }
    }
}

void print_matrix(double **A) {
    for (int i = 0; i < N; ++i) {
        for (int j = 0; j < N; ++j) {
            printf("%5.5f ", A[i][j]);
        }
        printf("\n");
    }
}


